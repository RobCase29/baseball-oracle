import type { CareerArcPoint, PlayerForecast } from '../domain/forecast'

function buildCareerArc(
  debutAge: number,
  lowOutcome: number,
  medianOutcome: number,
  highOutcome: number,
  actual: Record<number, number> = {},
): CareerArcPoint[] {
  return Array.from({ length: 19 }, (_, index) => {
    const age = 20 + index
    const elapsed = Math.max(0, age - debutAge + 1)
    const careerLength = 39 - debutAge
    const share = Math.min(1, Math.pow(elapsed / careerLength, 0.72))

    return {
      age,
      low: Number((lowOutcome * share).toFixed(1)),
      median: Number((medianOutcome * share).toFixed(1)),
      high: Number((highOutcome * share).toFixed(1)),
      ...(actual[age] === undefined ? {} : { actual: actual[age] }),
    }
  })
}

